public class LogicalOperator {
    public static void main(String[] args) {
        boolean val1 = true;
        boolean val2 = false;
        System.out.println(val1&&val2);
        int num1=12;
        int num2=10;
        System.out.println((num1>num2)||(num1<num2));
        System.out.println(!(num1>num2));
    }
}
